    <p>
    Urządzenia mobilne towarzyszą nam każdego dnia, smartfon stał się swoistym centrum zarządzania, służąc jako podstawowe narzędzie do kontaktu ze światem. Tymczasem według badań, wiele osób w ogóle nie zabezpiecza swojego sprzętu oraz nigdy nie zmienia hasła. Wiedzą o tym przestępcy, którzy jako cel chętnie obierają właśnie urządzenia mobilne. Na szczęście stosowanie się do kilku prostych zasad może znacznie zwiększyć bezpieczeństwo nasze i naszych urządzeń.

    </p>
    <p>
    Według zeszłorocznego raportu firmy Bitdefender<sup>1</sup> 11% właścicieli smartfonów w ogóle ich nie zabezpiecza, a 27% wykorzystuje do tego bardzo prosty kod PIN (np. 1111 lub 1234). Dzieje się tak, mimo że ponad 60% respondentów twierdzi, że doświadczyło w ciągu ubiegłego roku przynajmniej jednego cyberzagrożenia, związanego z telefonem komórkowym. Podobne badania przeprowadzane wśród Polaków nie pokazują nas niestety w lepszym świetle. Jak wynika z analizy przeprowadzonej przez Quality Watch na zlecenie Biura Informacji Kredytowej<sup>2</sup> aż jedna czwarta Polaków nie zabezpiecza hasłem swoich smartfonów. Wszystko to w sytuacji, gdy to właśnie urządzenia mobilne stały się głównym celem ataków cyberprzestępców.

</p>
   
    <img src="/img/articles/bezpieczne_urzadzenia.jpg" class="article-img" alt="xxxxxx"/>

    <h2 class="subpage-subtitle" id="jak-atakowane-sa-telefony">Jak atakowane są telefony?</h2>

<p>
Po zainfekowaniu, nasz sprzęt może być wykorzystany do kradzieży danych, oszustw finansowych, a nawet do bezpośrednich ataków na przedsiębiorstwa, w których zatrudnieni są właściciele zaatakowanych smartfonów. Przestępcy wykorzystują cały arsenał, w jaki uzbroili się przez lata przy atakach przeciwko komputerom – stosują phishing, wysyłają fałszywe SMS-y z linkami prowadzącymi do szkodliwych stron, wykorzystują socjotechnikę, by zdobyć nasze zaufanie. Czy jesteśmy zatem bezbronni? Niekoniecznie. <strong>Stosując się do kilku podstawowych zasad możemy bardzo utrudnić zainfekowanie naszego telefonu i zneutralizować większość najczęściej stosowanych, masowych metod ataków.</strong>
</p>
<p>
Smartfony stały się nieodłącznym towarzyszem naszej codzienności, to na nich przechowujemy nasze osobiste, często wrażliwe dane. Służą nam jako podstawowy kanał kontaktu z bankiem. Dziś właśnie nim autoryzujemy przelewy czy dokonujemy płatności w sklepach. Na co powinniśmy więc zwrócić uwagę, by nie stać się ofiarą cyberprzestępców? Przede wszystkim świadomie korzystajmy z naszego smartfona i stosujmy się do zasady ograniczonego zaufania. Powinniśmy stosować ją w każdym kanale komunikacji – połączeniach głosowych, e-mail, sms czy odwiedzanych stronach internetowych. </p>
<p>Zawsze zachowujmy ostrożność i weryfikujmy nadawcę lub adres strony internetowej. I najważniejsze: musimy uświadomić sobie, że numer telefonu, czy adres mailowy, wyświetlany na ekranie naszego telefonu, niekoniecznie musi być prawdziwy – cyberprzestępcy wiedzą jak podszyć się pod numer nadawcy, któremu ufamy, dlatego z uważnością podchodźmy do wszelkiego rodzaju otrzymywanych wiadomości lub telefonów, w których jesteśmy proszeni o poufne informacje lub instalację aplikacji lub oprogramowania. W sytuacji, w której nie mamy pewności, czy są one prawdziwe,  oddzwońmy do nadawcy, wybierając jego numer na klawiaturze telefonu.</p>

    <h2 class="subpage-subtitle" id="co-jeszcze-mozemy-zrobic">Co jeszcze możemy zrobić, by chronić swój sprzęt mobilny? Oto kilka rad, o których warto pamiętać.</h2>
<ul>
<li>Instaluj i aktualizuj oprogramowanie antywirusowe, pamiętaj, że operatorzy często udostępniają je za darmo lub za minimalną opłatą.</li>
<li>Dbaj o regularne aktualizacje wszystkich aplikacji, z których korzystasz – możesz w ten sposób uniknąć wykorzystania przez przestępców luk w aplikacjach.</li>
<li>Nie instaluj aplikacji z niepewnego źródła - zawsze upewnij się też, jakie uprawnienia dajesz instalowanemu programowi.</li>
<li>Nie otwieraj załączników czy odsyłaczy, jeśli nie znasz nadawcy.</li>
<li>Zwracaj uwagę na adres strony internetowej lub adres nadawcy wiadomości e-mail.</li>
<li>Nie klikaj w linki załączone w wiadomościach SMS, jeśli nie masz całkowitej pewności, co do nadawcy. Pamiętaj, że numery nadawców wiadomości przychodzących mogą być fałszywe (tzw. spoofing).</li>
<li>Nie odbieraj też połączeń z numerów zagranicznych, których nie znasz i nie oddzwaniaj na takie numery.</li>
<li>Staraj się nie korzystać z publicznych, otwartych sieci Wi-Fi – doświadczony przestępca może przechwycić Twoje prywatne dane, w tym loginy i hasła.</li>
<li>Unikaj prostych, łatwych do odgadnięcia kodów PIN.</li>
<li>Jeśli korzystasz ze smartfona w celach służbowych, sprawdź,  jakie dodatkowe zabezpieczenia oferuje Twoja organizacja.</li>

</ul>
