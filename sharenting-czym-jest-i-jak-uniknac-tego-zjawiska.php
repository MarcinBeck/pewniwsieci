  <p>
  Rozmaitymi momentami z życia rodzinnego uwielbiamy dzielić się z bliskimi i przyjaciółmi. Na co dzień sieć bardzo ułatwia nam to zadanie, dlatego tak chętnie czerpiemy z możliwości oferowanych przez nią platform. W rezultacie 4 na 5 dzieci w wieku do 2 lat ma już w internecie swój ślad cyfrowy <sup>1</sup>. Wykorzystywanie ich wizerunku w mediach społecznościowych niekiedy prowadzi jednak do sharentingu, na który warto zwrócić obecnie szczególną uwagę. Na czym polega to zjawisko i jak go uniknąć?
</p>

<img src="img/articles/sharenting.jpg" class="article-img" alt="Sharenting"/>

<p>Przestrzeń cyfrowa pozwala nam być blisko siebie nawet wtedy, gdy dzielą nas kilometry. Trudno odmówić jej więc szeregu zalet w aspekcie budowania i podtrzymywania relacji. Warto jednak pamiętać o dobrych praktykach, które pomogą nam korzystać z cyfrowych udogodnień w sposób odpowiedzialny i w efekcie zminimalizować ryzyko potencjalnych zagrożeń. To istotne zwłaszcza w kontekście życia rodzinnego. Dlaczego?</p>

<h2 class="subpage-subtitle" id="od-parentingu-do-sharentingu">Od parentingu do sharentingu</h2>
<p>
W sieci najchętniej dzielimy się tym, co wokół nas. A towarzyszami naszych codziennych przygód często pozostają członkowie rodziny i ostatecznie to oni stają się tłem lub wręcz pierwszoplanowymi bohaterami publikacji, które umieszczamy w social mediach. Od parentingu, czyli materiałów związanych z rodzicielstwem, niedaleko jednak do sharentingu. Na czym polega to ostatnie zjawisko?</p>
<p>Z sharentingiem mamy do czynienia wówczas, gdy dzielimy się w internecie wrażliwymi treściami związanymi z naszymi dziećmi. Mogą to być zdjęcia i opisy sytuacji, które w danym momencie uznaliśmy za zabawne, opowieści o trudach wychowawczych czy słodkie fotografie, na których nasza pociecha prezentuje się wyjątkowo uroczo. Każdy taki post, film czy obraz umieszczone w sieci niosą jednak za sobą pewne ryzyko, które przy publikacji zawsze powinniśmy wziąć pod uwagę. Niestety zdarzają się nawet takie sytuacje, kiedy materiały z wizerunkiem dzieci trafiają w ręce pedofili i są np. przez nich sprzedawane na czarnym rynku jako pornografia dziecięca. 
</p>

<h2 class="subpage-subtitle" id="siec-informacji">Sieć informacji</h2>

<p>Mówi się, że w przestrzeni internetowej nic nie ginie i bywa w tym sporo prawdy. W momencie udostępnienia danej treści tracimy bowiem kontrolę nad jej potencjalnym wykorzystaniem. Wystarczy szybki zrzut ekranu, by zapisać na zawsze zdjęcie, relację czy komentarz. Taki materiał może później pojawić się na innych profilach w sieci i stać się podstawą do publikacji kolejnych, wykorzystujących wizerunek pociechy postów. Zjawisko „cyfrowego kidnapingu”, czyli baby role play to przestępstwo polegające na używaniu skradzionego wizerunku dziecka do realizacji różnych fantazji, w tym seksualnych lub przemocowych. Jak się okazuje, neutralna początkowo fotografia, może stać się pożywką dla osób z zaburzeniami lub pedofili. </p>
<p>Innym niepożądanym efektem naszych działań bywa surowa ocena przez rówieśników syna lub córki. Przeszło 30% nastolatków doświadcza w sieci hejtu z powodu wyglądu<sup>2</sup>, a ataki te mogą dotyczyć również treści z przeszłości, ukazujących wcześniejsze etapy dorastania. Jednym z takich zjawisk jest tzw. troll parenting, czyli rozpowszechnianie treści, które upokarzają czy kompromitują dziecko, poprzez pokazywanie trudnych dla niego sytuacji, doświadczeń, a nawet scen przemocy. </p>
<p>W ostatnich latach, m.in. w Austrii, Calgarze oraz we Włoszech, mamy przy tym do czynienia z pierwszymi procesami, które młode osoby wytaczają swoim rodzicom z powodu przykrych doświadczeń związanych z wykorzystaniem wizerunku. Ze względu na rosnącą skalę zjawiska sharentingu także francuski parlament pracuje nad stworzeniem przepisów, które ograniczą możliwość nadużywania materiałów ukazujących dzieci bez ich zgody.</p>


<h2 class="subpage-subtitle" id="rodzina-bezpieczna-w-sieci">Rodzina bezpieczna w sieci</h2>
<p>Co zatem możemy zrobić, aby wszyscy członkowie rodziny czuli się w przestrzeni wirtualnej komfortowo? Warto wziąć pod uwagę kilka wskazówek:</p>
<ol>
<li>Zadbaj o odpowiednie ustawienia prywatności swojego profilu w mediach społecznościowych, aby wgląd do zamieszczanych na nim treści miały jedynie najbliższe, sprawdzone osoby. </li>
<li>Nie publikuj zdjęć i filmów, które teraz lub w przyszłości mogą okazać się ośmieszające albo zawstydzające dla dziecka. Zabawne zachowanie lub mina bywają rozczulające, ale inaczej zinterpretują je np. szkolni koledzy i koleżanki czy przyszły pracodawca.</li>
<li>Nie udostępniaj materiałów, które są intymne i pokazują np. nagość dziecka. Nigdy nie mamy pewności, kto może przechwycić te treści.</li>
<li>Nie udzielaj informacji o życiu rodzinnym w czasie rzeczywistym. Dane o wakacyjnym wyjeździe czy porach pobytu w różnych miejscach mogą zostać wykorzystane przez przestępców przeciw dziecku lub całej rodzinie.</li>
<li>Nie ukazuj dziecka w postach w negatywnym świetle. Nawet jeśli to tylko humor, może nieść za sobą przykre konsekwencje.</li>
<li>Spytaj dziecko o zgodę na publikację. Jeśli jest już wystarczająco duże, by wyrazić swoją opinię, warto pozostawić mu finalną decyzję.</li>
<li>Jeżeli chcesz wysłać nastolatkowi zaproszenie do znajomych na portalu społecznościowym, uzgodnij z nim to wcześniej i uszanuj jego decyzję. W wypadku, gdy jakieś treści na profilu Cię zaniepokoją, porozmawiaj z dzieckiem w cztery oczy, poza wirtualną przestrzenią.</li>
</ol>


<p>W odpowiedzi na tę sytuację Deutsche Telekom stworzyło kampanię „Share with care", która ma pomóc w zwiększaniu świadomości na temat zagrożeń i zachęcić do odpowiedzialnego korzystania z mediów społecznościowych. Najnowszy film dobitnie ilustruje znaczenie ochrony danych naszych dzieci.</p>
