    <h1 class="subpage-title">Niebezpieczne treści dla dzieci. Jak możemy chronić dzieci przed pornografią oraz innym niebezpiecznymi materiałami w internecie?</h1>
    <nav id="subpage-nav">
      <ul >
        <li><a href="#niebezpieczenstwa-zwiazane-z-ogladaniem-nieodpowiednich-tresci-przez-najmlodszych">Niebezpieczeństwa związane z oglądaniem nieodpowiednich treści przez najmłodszych</a></li>
        <li><a href="#rozmawiaj-i-buduj-zaufanie">Rozmawiaj i buduj zaufanie</a></li>
      </ul>
    </nav>
  </div>
  <p>
  Internet wplótł się płynnie w tkankę naszego życia, oferując wszystkim powszechnie dostępne pokłady wiedzy, łączności i rozrywki. Jest on zarówno wspaniałym miejscem o nieskończonych możliwościach, ale też krajobrazem ukrytych zagrożeń, takich jak niebezpieczne materiały wideo, po które bez trudu może sięgnąć każde dziecko czy nastolatek. Oglądanie tego typu treści przez małoletnich użytkowników sieci może prowadzić do poważnych konsekwencji. Jak czytamy w raporcie NASK „Nastolatki wobec pornografii cyfrowej” - dzieci korzystające z pornografii, mają niższy stopień integracji społecznej i wyższy poziom zachowań niepożądanych. Obserwuje się też u nich częstsze występowanie objawów depresyjnych oraz zmniejszoną więź emocjonalną z opiekunami<sup>1</sup>. Czym charakteryzują się te niebezpieczne materiały w internecie? W jakim wieku dzieci po raz pierwszy mają styczność z pornografią? Jak często nastolatkowie oglądają patotreści? Jak rozmawiać z dziećmi, aby uświadomić im zagrożenia wynikające z niewłaściwego korzystania z internetu? Na te pytania i nie tylko odpowiemy w poniższym artykule. 
</p>

<img src="img/articles/niebezpieczne-tresci.jpg" class="article-img" alt=""/>


<p>Zbadanie zawiłej sieci wyzwań związanych z łatwą dostępnością pornografii oraz innych niewłaściwych treści w internecie, szczególnie dla młodszych pokoleń, nie jest prostym zagadnieniem. Po pierwsze – coraz więcej badań wskazuje na zmiany w postrzeganiu relacji międzyludzkich przez osoby oglądające treści dla dorosłych. Jak wynika z raportu NASK „Nastolatki wobec pornografii cyfrowej”, czynności seksualne, do których przymuszono drugą osobę, widział w internecie prawie co trzeci 16-latek (29,8%) i co piąty (19,2%) badany w wieku 12-14 lat<sup>2</sup>. Ponadto już 37% nastolatków w wieku 13-15 lat przyznaje, że oglądało patotreści w internecie<sup>3</sup>, trudno więc dziwić się, że coraz więcej młodych osób nie potrafi realnie ocenić odpowiednich zachowań w rzeczywistości – a co gorsza, nie potrafi dostrzec odpowiednich wzorców, które powinni internalizować do swojego życia. </p>

<h2 class="subpage-subtitle" id="niebezpieczenstwa-zwiazane-z-ogladaniem-nieodpowiednich-tresci-przez-najmlodszych">Niebezpieczeństwa związane z oglądaniem nieodpowiednich treści przez najmłodszych</h2>
<p>Żyjemy w świecie, w którym nowe technologie pozwalają dzieciom i młodzieży poszerzać swoją wiedzę i rozwijać zainteresowania. Dzięki internetowi mają dostęp np. do native speakerów z całego świata, do stron poświęconych rozwijaniu ich zainteresowań czy podnoszenia kwalifikacji. Ogromna moc sieci internetowej w zakresie edukacji czy rozwoju zainteresowań idzie w parze z jej potencjałem do nieumyślnego narażania nieletnich na treści, na które nie są gotowi psychicznie i emocjonalnie. Jednym z zagrożeń, na jakie mogą natrafić młodzi użytkownicy internetu, są patotreści, czyli różnego rodzaju materiały, które mogą wywoływać u odbiorców negatywne emocje lub promują niebezpieczne zachowania. Tego typu przekazy przybierają różne formy – od zdjęć, przez filmy, po streamingi, ukazujące sprzeczne z normami społecznymi zachowania, np. przemoc fizyczną i psychiczną, poniżanie czy libacje alkoholowe. </p>
<p>Innym, bardzo niebezpiecznym zjawiskiem, jest seksting. Na czym on polega? Na wysyłaniu innym osobom swoich intymnych zdjęć bądź filmów. Niestety coraz częściej zjawisko to jest akceptowalne wśród młodych użytkowników internetu. Co piąty z nich (18,6%) nie widzi nic złego w takim zachowaniu<sup>4</sup>. Młodzi użytkownicy sieci nie zdają sobie sprawy, jakie to może nieść konsekwencje, a trzeba pamiętać o tym, że w internecie nic nie ginie. Więc zdjęcie, które teoretycznie miało trafić tylko do jednej osoby, nagle może być dostępne na publicznym profilu. Zjawisko to wiąże się często z hejtem ze strony rówieśników, ale także zastraszaniem czy szantażowaniem. Czy są więc jakieś przepisy, które regulują dostęp do niebezpieczych treści w internecie? </p>





<h2 class="subpage-subtitle" id="rozmawiaj-i-buduj-zaufanie">Rozmawiaj i buduj zaufanie</h2>

<p>Należy pamiętać o tym, że choć dzieci i młodzież mają dużą swobodę w korzystaniu z internetu i mediów społecznościowych, to warto z nimi rozmawiać i uświadamiać ich na temat niebezpieczeństw, jakie na nich czyhają w sieci. Jak podaje raport NASK, jedynie 23,8% ankietowanych rozmawiało ze swoimi rodzicami o zjawisku pornografii w sieci<sup>5</sup>, więc z pewnością jest to przestrzeń, którą należy zaopiekować. </p>
<ol>
  <li>Rozmowa jest kluczowa – takie proste, a takie prawdziwe. Z drugiej strony rozmowa o seksie, intymności nie jest łatwa, więc nie dziwi fakt, że rodzice obawiają się poruszać tego tematu. Warto budować z dzieckiem relację opartą na wzajemnym zaufaniu oraz pokazać mu, że może zapytać dorosłych o dosłownie wszystko – dzięki czemu rozmowy na tzw. tematy tabu będą mniej krępujące. Niemniej bez uświadomienia dzieci o tym, czym jest pornografia i dlaczego należy unikać tego typu treści, one same tej wiedzy nie zdobędą.</li>
  <li>Blokada stron www – w ramach różnych usług u operatorów, rodzic może włączyć opcję, w której zablokowany zostanie dostęp do niewłaściwych dla dziecka czy nastolatka stron internetowych. To pozwoli- przynajmniej na jakiś czas – zahamować niewłaściwe zainteresowania. </li>
  <li>Wspólnie spędzony czas – jedną z podstawowych przyczyn dla których młodzież sięga po pornografię, jest nuda (deklaruje tak 25% ankietowanych)<sup>6</sup> – dlatego ważne jest, aby wspierać dziecko w odkrywaniu różnych możliwości spędzania czasu wolnego, a najlepiej spędzać go rodzinnie. Dzięki temu też uczy się ono odpowiednich wzorców relacji międzyludzkich, zacieśnia więzi z rodzicami i rodzeństwem. </li>
</ol>
<p>To tylko kilka prostych kroków, dzięki którym można ustrzec dzieci i młodzież przed pułapką nieodpowiednich treści w internecie. Podstawą jest więc edukacja i budowanie zaufania między dzieckiem a rodzicami. </p>
