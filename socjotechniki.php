    <p>
    Jedną z charakterystyk nowoczesnego ataku hakerskiego są socjotechniki – techniki wpływu społecznego (lub manipulacji) stosowane przez m. in. cyberprzestępców i oszustów mające na celu spowodowanie, że dana osoba lub ktoś z jej otoczenia zrobi to, czego od niego potrzebują. Czym dokładnie są i jak możemy się przed nimi chronić?

    </p>
    <h2 class="subpage-subtitle" id="socjotechniki-jaki-jest-ich-mechanizm-i-dlaczego-sa-skuteczne">Socjotechniki – jaki jest ich mechanizm i dlaczego są skuteczne?</h2>
    <p>
    Socjotechniki, znane także jako inżynieria społeczna (eng. social engineering) to metody wpływania na ludzi w taki sposób, by osoba atakowana zrobiła to, czego potrzebuje od niej atakujący, np.: przekazała poufne informacje czy też dała dostęp do miejsca, które powinno być chronione. Doświadczony oszust wie, że przeciętny użytkownik może nie zwrócić uwagi na podrobioną witrynę logowania „swojego banku”, jeśli otrzyma komunikat, który wywoła potrzebę nagłej reakcji i przekonania, że może wiele stracić np. „wykryto nieautoryzowany przelew na konto za granicą”. Mechanizm działania skupia się na błędach popełnianych przez człowieka, ale jednocześnie każdy z nas ma możliwość zadbania o to, by być bezpiecznym. W tym przypadku, najlepszym narzędziem ochrony jest wiedza. 
    </p>
    <img src="img/articles/socjotechniki.jpg" class="article-img" alt="Socjotechniki"/>

    <p>
    Musimy więc pozbyć się obrazu hakerów jako zakapturzonych przestępców, siedzących w ciemnej piwnicy przed ekranem zalanym zielonymi znakami 😉 Zauważmy, że duża część ich działań opiera się na  znajomości psychologii. Gra na emocjach, wywoływanie strachu, poczucia nagłości czy stresu – to wszystko narzędzia w arsenale kompetentnego hakera. Bądźmy więc czujni na wiadomość, połączenie czy jakąkolwiek inną formę interakcji, która powoduje, że czujemy się ponaglani i namawiani do potencjalnie ryzykownych działań. Może być to znak, że właśnie jesteśmy celem ataku opartego na socjotechnikach.
</p>



<h2 class="subpage-subtitle" id="jak-sie-przed-tym-chronić">Jak się przed tym chronić?</h2>
<p>Jak się przed tym chronić? Wystarczy pamiętać o poniższych krokach i je stosować.</p>

<ol>
<li>Po pierwsze i najważniejsze – weź głęboki oddech i chwilę się zastanów. Czy ta osoba naprawdę potrzebuje, żebyś podał/a jej hasło przez telefon? Czy jakakolwiek instytucja może w ogóle zapytać o login i hasło? Czy sprawa faktycznie jest tak nagła, jak twierdzi rozmówca? Czy naprawdę zamkną Ci konto w banku, jeśli się rozłączysz? Wiele z rzeczy wmawianych przez oszustów zwyczajnie nie ma sensu – ale trudno to dostrzec, jeśli górę bierze stres.</li>
<li>Poddawaj w wątpliwość – czasami wystarczy spytać „dlaczego tego ode mnie potrzebujesz?”, żeby wprawić oszusta w zakłopotanie. Oczywiście dobrze przygotowany rozmówca będzie miał na to gotową odpowiedź, ale weryfikacja i zdrowa doza nieufności zdecydowanie działają tu na naszą korzyść.</li>
<li>Używaj alternatywnych dróg kontaktu – dzwoni do Ciebie ktoś i twierdzi, że jest konsultantem banku, a na Twoim koncie pojawił się nagły problem? Nie zgadza się numer (czasami może nawet wyglądać na poprawny – więcej o tym w artykule o spoofing’u)? Zastosuj wymówkę, np. „przepraszam, ale nie mogę w tej chwili rozmawiać – oddzwonię” i zadzwoń na niezależnie sprawdzony, własnoręcznie wybrany, poprawny numer. Poinformuj przedstawiciela instytucji o zdarzeniu – jeśli połączenie było prawdziwe i faktycznie wystąpił problem, rozwiążecie go. Jeśli jednak był to ktoś nieautoryzowany, powiadomisz instytucję o zagrożeniu, którego właśnie uniknąłeś/aś – możliwe, że pomożesz komuś, kto go nie rozpoznał 😊</li>
<li>Weryfikuj – przeczytaj uważnie maila lub SMSa z „POWAŻNYM OSTRZEŻENIEM”. Sprawdź w internecie numer, z którego do Ciebie dzwoniono, zanim podasz jakiekolwiek informacje. Zapytaj o informacje dotyczące Ciebie, które powinna znać tylko ta instytucja. Każda z tych informacji to pole do błędu dla przestępcy – a zauważenie nieprawidłowości może dać Ci pewność, że należy być ostrożnym.</li>
</ol>
