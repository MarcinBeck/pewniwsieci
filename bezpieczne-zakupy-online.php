    <p>
    Któż z nas choć raz w życiu nie dokonał zakupów w sklepie internetowym? Doceniamy, że to rozwiązanie jest wygodne, proste, a często można jeszcze zaoszczędzić czas i pieniądze. Jednak czy wiesz, jak dokonywać takich zakupów w sieci w sposób bezpieczny i na co zwracać uwagę na stronach internetowych? Poznaj kilka ważnych zasad, które przygotowaliśmy w poniższym artykule. 

    </p>
    <p>
Według badania przygotowanego przez Gemius<sup>1</sup>, w 2021 roku 77 proc. internautów dokonało zakupu choćby jednej rzeczy online. Niewątpliwie miał na to wpływ rok 2020 i pandemia Covid-19, która „nauczyła”, czy też ośmieliła Polaków do robienia zakupów online. Z jednej więc strony można zaobserwować znaczący wzrost zainteresowania tą formą nabywania dóbr i produktów, a z drugiej – rozszerzone pole do działania dla cyberprzestępców. Czy potrafisz w bezpieczny sposób dokonywać zakupów i płatności online? Co powinno wzbudzić Twoją uwagę na internetowych witrynach sklepów? 
</p>
    <img src="/img/articles/bezpieczne_zakupy_online.jpg" class="article-img" alt="Bezpieczne zakupy online"/>

    <h2 class="subpage-subtitle" id="nie-daj-sie-nabrac-na-wyjątkowe-promocje">Nie daj się nabrać na „wyjątkowe promocje”</h2>
<p>
Poszukując w internecie wybranego produktu często sprawdzamy, gdzie daną rzecz możemy kupić najtaniej. Naturalne jest, że chcemy po prostu zaoszczędzić, jednakże widząc cenę znacznie niższą, powinniśmy zastanowić się dwa razy, zanim dokonamy zakupu. Dlaczego? Otóż może być to zasadzka przygotowana przez cyberprzestępców, którzy liczą – niestety – na naiwność współczesnych konsumentów. W kilka chwil możemy stracić pieniądze, których odzyskanie potem może być wręcz niemożliwe. 
</p>
<h2 class="subpage-subtitle" id="sprawdz-strone-sklepu-i-jego-adres-online">Sprawdź stronę sklepu i jego adres online</h2>

<p>
Planując zakupy w sklepach online, zwłaszcza tych mniej znanych, warto sprawdzić podstawowe dane o tej wirtualnej przestrzeni, takie jak fizyczny adres czy NIP. Ponadto każdy tego typu podmiot powinien mieć umieszczony na stronie internetowej regulamin, w którym jasno zapisane są zasady zakupu, zwrotu czy reklamacji. Istotną kwestią jest też sama nazwa strony internetowej – przed podaniem swoich danych, a zwłaszcza danych do płatności, sprawdź, czy połączenie jest szyfrowane tj. czy nazwa strony zaczyna się od https:// lub czy widnieje przy adresie symbol kłódki.</p>



<h2 class="subpage-subtitle" id="pospiech-zlym-doradca">Pośpiech złym doradcą</h2>

<p>
Spotkałeś/aś się z informacją, że promocja na dany produkt – akurat ten, którego szukałeś/aś – kończy się za 10 min i pozostało Ci już niewiele czasu, aby dokonać zakupu? Jeśli tak, to taki komunikat powinien wzbudzić czujność, ponieważ takie „wymuszanie” dokonania transakcji z pewnością nie jest prokonsumenckim działaniem. Nie mówimy tu o limitowanych czasowo ofertach, które kończą się za tydzień lub do końca dnia podczas np. Black Friday. Chodzi o wymuszenie podjęcia natychmiastowej decyzji, która może uśpić naszą czujność. Niestety pośpiech nigdy nie jest dobrym doradcą, dlatego zanim dokonasz zakupu, odetchnij, złap dystans i sprawdź na spokojnie, czy ta oferta faktycznie jest bezpieczna.</p>

<h2 class="subpage-subtitle" id="nie-zapisuj-swoich-danych">Nie zapisuj swoich danych</h2>



<p>Choć nasze urządzenia często podpowiadają funkcję zapisywania wprowadzanych danych czy adresu, to zdecydowanie nie powinniśmy zgadzać się na to podczas wpisywania danych karty płatniczej. Dlaczego? Ponieważ jeśli dostęp do konta w danym sklepie zostanie przejęty przez cyberprzestępcę, to niestety nieuprawniona osoba będzie mogła dokonywać zakupów z naszych pieniędzy. A tego raczej nikt z nas nie chce doświadczyć. </p>
<p>Zakupy online ułatwiają nam codzienne funkcjonowanie – mamy dostęp do wybranych sklepów o każdej porze dnia i gdziekolwiek jesteśmy. Możemy oszczędzać czas i pieniądze, ale pod warunkiem, że będziemy te zakupy robić rozważnie i zwracać uwagę na powyższe aspekty. 😊</p>


