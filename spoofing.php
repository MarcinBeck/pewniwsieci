    <p>
    Świat, który nas otacza staje się coraz bardziej cyfrowy. Codziennie dostajemy dziesiątki lub setki powiadomień, wiadomości sms czy e-mail, logujemy się do kilku czy kilkunastu usług i stron internetowych. Niemal każdy z nas jest właścicielem smartfona, w którym ma zainstalowany przynajmniej jeden komunikator. Niestety, wykorzystują to przestępcy, którzy podszywają się pod firmy i instytucje, a czasem nawet pod naszych znajomych czy rodzinę. Czy możemy się przed tym chronić? 

    </p>
    <p>Czasy, kiedy internetowi przestępcy wykorzystywali do ataków niemal wyłącznie złośliwe oprogramowanie czy wirusy dawno już minęły. Obecnie, najchętniej wykorzystują znacznie prostszą, a przy tym równie skuteczną metodę – socjotechnikę. To już nie tylko proste wyłudzenia danych, wykorzystujące np. naiwność rozmówcy. Coraz częściej mamy do czynienia z wiadomościami, e-mailami czy telefonami, które do złudzenia przypominają takie, które moglibyśmy dostać od banku, ZUSu, czy od znajomego. Co więcej, na pierwszy rzut oka taka wiadomość wydaje się być wysłana z prawdziwego numeru telefonu czy adresu e-mail. To zjawisko zwane spoofing’iem. </p>
   
    <img src="/img/articles/spoofing.jpg" class="article-img" alt="Spoofing"/>

    <h2 class="subpage-subtitle" id="spoofing-czyli-co">Spoofing, czyli co?</h2>

    <p>Spoofing to oszustwo, w którym przestępca podszywa się pod jakąś osobę, firmę lub instytucję. Celem takich działań może być kradzież danych lub przekonanie ofiary do zainstalowania złośliwego oprogramowania czy aplikacji, w celu pozyskania przez przestępcę dostępu do konta bankowego. </p>

    <p>Spoofing może dotyczyć np. adresu IP (wysyłanie komunikatów do komputera z adresu IP, który sugeruje, że wiadomość pochodzi z zaufanego źródła), adresu e-mail (przestępcy podrabiają adres i nagłówek wiadomości) czy domeny – gdy podmianie podlega adres serwera DNS, przez co ruch zamiast na prawdziwą stronę internetową, przesyłany jest na jej specjalnie spreparowaną wersję. </p>

    <p>Nie mniej groźne bywają przypadki spoofingu telefonicznego, najczęściej przy wykorzystaniu tzw. Caller ID spoofing, kiedy to przestępcy podszywają się pod cudzy numer telefonu w taki sposób, że widzimy połączenie np. od banku (taki numer i nazwa wyświetla się w naszym telefonie), a zamiast przedstawiciela tej instytucji rozmawiamy z oszustem, który próbuje wydobyć od nas poufne informacje takie jak dane potrzebne do logowania, czy dane osobowe potrzebne do operacji bankowych, czy autoryzacji transakcji. Najczęściej jednak spoofing numeru telefonu bywa wykorzystywany do wyłudzenia danych lub skłonienia rozmówcy do ujawnienia haseł, lub dokonania przelewu (np. przez popularny system BLIK).</p>



    <h2 class="subpage-subtitle" id="jak-sie-chronic">Jak się chronić?</h2>
    <p>Pamiętajmy, że żaden bank, dostawca mediów czy sklep internetowy nie zażąda od nas podawania loginów i haseł w formie innej, niż przy logowaniu się do ich systemu. Podobnie, w wypadku odwiedzin stron internetowych, wymagających wprowadzania hasła, wrażliwych danych osobowych, upewnijmy się zawsze, czy nie wyglądają one podejrzanie, czy połączenie jest szyfrowane i czy informacje, których się od Ciebie żąda, rzeczywiście są niezbędne.</p>
<p>Nie odpowiadajmy na e-maile z tego typu pytaniami, a każdą próbę zgłaszajmy swojemu administratorowi lub operatorowi. Dokładnie sprawdzajmy, czy adres nadawcy wiadomości, w której proszeni jesteśmy o podanie danych, jest właściwy. Przede wszystkim jednak każdorazowo weryfikujmy nadawcę lub adres strony internetowej. Szczególną czujność zachowajmy w przypadku wiadomości e-mail, której nadawca prosi o kliknięcie w link, pobranie lub otworzenie pliku lub przeprowadzenie działań związanych z przelewami lub dostępem do systemów i usług. </p>

<p>Pamiętajmy, że przestępcy mają możliwość podszycia się pod cudzy adres e-mail lub konkretny numer telefonu (np. numer identyfikujący się jako bank czy policja, wyświetlany jako numer przechowywany w naszych urządzeniach). Warto więc przestrzegać kilku, podstawowych zasad:</p>
<ul>
<li>Nie działaj pod presją czasu! Uważaj na wszelkie próby uzyskania danych (telefonicznie, SMS-em, e-mailem), które wymagają szybkiej odpowiedzi. Przestępcy bazują na nieuwadze ofiary.</li>
<li>Nigdy nie przekazuj przez telefon lub e-mail poufnych danych, które umożliwiają dostęp do bankowości internetowej lub jakiejkolwiek innej strony (hasła, loginy, numery kart). Pamiętaj, że administratorzy nigdy nie zapytają Cię o dane logowania. Instytucje, takie jak banki w inny sposób weryfikują rozmówcę.</li>
<li>Sprawdzaj nagłówki wiadomości e-mail, w wypadku wątpliwości skontaktuj się z administratorem swojej poczty.</li>
<li>Jeśli masz jakiekolwiek wątpliwości, że telefon pochodzi z fałszywego numeru rozłącz się i skontaktuj z odpowiednią organizacją, wybierając jej numer ręcznie, na klawiaturze telefonu.</li>
<li>Jeśli potencjalnie fałszywe połączenie pochodzi od znajomego, ale masz jakiekolwiek podejrzenia (na przykład nie rozpoznajesz głosu), rozłącz się i oddzwoń za pomocą klawiatury numerycznej.</li>
<li>Podczas rozmowy telefonicznej bądź świadomy sytuacji, które mogą budzić podejrzenia lub niepokój. Najczęściej rozmówcy proszą nas o dane wrażliwe niedługo po nawiązaniu połączenia (hasła, dane konta, dane osobowe) i próbują nas naciskać. Do każdej tego typu sytuacji podchodź ze spokojem, jeśli masz wątpliwości – rozłącz się.</li>

</ul>
