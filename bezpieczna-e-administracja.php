  <p>
  Cyfryzacja stopniowo obejmuje rozmaite obszary naszego funkcjonowania, tworząc nowe możliwości edukacji, pracy, rozrywki czy kontaktu z bliskimi. Nie inaczej jest w przypadku spraw administracyjnych, które coraz częściej możemy załatwić online. W codziennym życiu niezastąpione bywają także elektroniczne odpowiedniki dokumentów. Jak dziś wygląda paleta e-rozwiązań państwowych, co umożliwia i czy nowe technologie są w tym kontekście bezpieczne? Warto przyjrzeć się zagadnieniu dokładnie, by efektywnie i bez obaw korzystać z cyfrowych udogodnień.
</p>

<img src="/img/articles/phishing.jpg" class="article-img" alt="Phising"/>

<p>Cyfryzacja państwa i urzędów to proces, który toczy się konsekwentnie od lat. W efekcie powstają kolejne e-bazy, które stale poszerzają się o nowe zasoby, ułatwiające funkcjonowanie administracji, zwiększające dostępność danych, a także poziom ich ochrony. I choć wszystkie obszary rozwoju cyfrowej Polski pozostają równie istotne, z perspektywy mieszkańców naszego kraju prawdziwa rewolucja rozpoczęła się, kiedy nad Wisłą zaczęły powstawać udogodnienia stworzone specjalnie z myślą o obywatelach. Ich popularność od samego początku wzrastała zresztą w błyskawicznym tempie – przykładowo liczba użytkowników Profilu Zaufanego między 2016 a 2023 rokiem zwiększyła się z ok. 400 tysięcy do ponad 16 milionów<sup>1</sup>. A to tylko jedno z dostępnych w przestrzeni wirtualnej rozwiązań, które realnie mogą ułatwić nam życie.</p>

<h2 class="subpage-subtitle" id="e-recepta-e-podatki-i-mobywatel">E-recepta, e-podatki i mObywatel</h2>

<p>Platformy i technologie, które wspierają obywateli, obejmują dziś już kilka obszarów ich codziennego życia. Jedną z nich jest Portal Pacjenta, gdzie bez problemu znajdziemy e-recepty, skierowania do specjalistów, historię leczenia czy zwolnienia lekarskie. Upoważnimy również bliskich do informacji medycznych, a po wprowadzeniu danych, takich jak nr telefonu czy e-mail, otrzymamy recepty SMS-em lub do wirtualnej skrzynki pocztowej. Na podobnej zasadzie, na odrębnych platformach, możemy łatwo rozliczyć PIT w wersji online, napisać do urzędu, podpisać dokument, a to wszystko dzięki elektronicznym metodom potwierdzenia tożsamości. </p>
<p>Od 2017 roku dostępna jest także aplikacja mObywatel, w której znaleźć się może m.in. elektroniczny odpowiednik naszego prawa jazdy, legitymacja emeryta-rencisty czy legitymacja szkolna, a nawet Karta Dużej Rodziny czy niektóre legitymacje służbowe, takie jak np. legitymacja adwokacka (w przyszłości również legitymacje zawodów medycznych). Wystarczy przy tym przejść do kolejnej zakładki, by odszukać szereg przydatnych usług, od możliwości zgłoszenia naruszeń środowiskowych, poprzez złożenie wniosku o refundację podatku VAT za paliwo gazowe, aż po opcję sprawdzenia punktów karnych czy e-recept.</p>



<h2 class="subpage-subtitle" id="polska-coraz-bardziej-cyfrowa">Polska coraz bardziej cyfrowa</h2>
<p>Co ważne, w tym roku możliwości oferowane przez aplikację mObywatel uległy znacznemu poszerzeniu. W związku z tym obecnie bez problemu dodamy tam także np. mDowód, którym wylegitymujemy się na poczcie, w przychodni, podczas zawierania umowy z operatorem czy w sądzie. Jest to bowiem trzeci, obok dowodu osobistego i paszportu, obowiązujący dokument tożsamości. Jedną z nowości jest tymczasowe prawo jazdy, które w wersji cyfrowej dostępne jest już zaraz po zdanym egzaminie. Aplikacja została poszerzona również m.in. o e-pełnomocnictwa. Lista opcji – zarówno w przypadku dokumentów, jak i spraw, które załatwimy przy wsparciu mObywatela, konsekwentnie się wydłuża. W obliczu coraz szerszych uprawnień internetowych narzędzi możemy jednak zastanawiać się, czy takie rozwiązanie jest w pełni bezpieczne.</p>

<h2 class="subpage-subtitle" id="ochrona-danych-w-swiecie-e-uslug-publicznych">Ochrona danych w świecie e-usług publicznych</h2>

<p>
Internetowe narzędzia tworzone dla obywateli przetwarzają szereg informacji na ich temat. Z tego też powodu są wyposażone w zaawansowane, wysokiej klasy zabezpieczenia informatyczne. W przypadku aplikacji mObywatel jest to szyfrowanie danych, możliwość wykorzystania biometrii przy logowaniu, a także ustawienia logowania dwuskładnikowego. Od tego roku z pomocą tej aplikacji można ponadto logować się do innych portali oferujących usługi publiczne, takich jak wspomniany Portal Pacjenta. Wygenerowany kod stanowi szybką i niezwykle bezpieczną metodę weryfikowania tożsamości.</p>
<p>Warto jednak pamiętać, że nowoczesne i dobrze zabezpieczone rozwiązania to nie wszystko, a nasz poziom ochrony w sieci zależy również od naszej wiedzy i czujności. Z tego powodu dobrze uwzględnić kilka dodatkowych wskazówek:</p>

<ol>
  <li><strong>Zwróć szczególną uwagę na maile czy telefony z urzędu.</strong> W zależności od podanej przez nas formy kontaktu zdarza się, że instytucje wysyłają np. wiadomość mailową. Zawsze jednak powinniśmy przeanalizować jej treść w poszukiwaniu nieprawidłowości takich jak literówka w adresie. Jeśli masz jakiekolwiek wątpliwości, najlepiej skontaktuj się bezpośrednio z daną jednostką inną drogą, np. za pomocą oficjalnej infolinii. W szczególności unikaj zaś klikania w niezweryfikowane linki i podawania informacji o sobie, dopóki nie upewnisz się, że to bezpieczny kontakt. Ten rodzaj zagrożenia omawialiśmy szerzej w naszych materiałach o <a href="/phishing">phishingu</a>.</li>
  <li><strong>Aktualizuj aplikacje</strong> – ponieważ nawet najbezpieczniejsze z narzędzi wymaga stałego unowocześniania zabezpieczeń. To najlepsza metoda, by uniknąć luk w ochronie wykorzystywanych przez cyberprzestępców.</li>
  <li><strong>Ściągaj aplikacje tylko z zaufanego źródła</strong> – jedynie w ten sposób uzyskasz gwarancję, że jest to bezpieczne narzędzie, a nie aplikacja-pułapka.</li>
</ol>

