    <h1 class="subpage-title">Nasze dane są niezwykle cenne - jak je chronić i zadbać o prywatność w sieci</h1>
    <nav id="subpage-nav">
      <ul >
        <li><a href="#warto-chronic-dane">Warto chronić dane</a></li>
        <li><a href="#jak-zatem-chronic-dane-osobowe-i-prywatnosc-w-sieci">Jak zatem chronić dane osobowe i prywatność w sieci?</a></li>
        
        

      </ul>
    </nav>
  </div>
    <p>
    Rodzinny samochód, przytulne mieszkanie, bogaty księgozbiór książek historycznych czy kosztowny laptop, który znakomicie sprawdza się w pracy - każdy z nas może do swojego majątku wliczyć coś innego. Ale łączy nas jedno - dobrze pilnujemy tego, co mamy najcenniejsze. Wiele osób zapomina zarazem, że na liście rzeczy materialnych nasze dobra się nie kończą. Zwłaszcza w czasach, w których coraz częściej korzystamy z nowych technologii a  transakcje finansowe realizujemy przez internet, powinniśmy zadbać o ochronę danych i prywatność w sieci. Jak zatem je skutecznie chronić?

    </p>
   <p>
   Żeby odpowiedzieć sobie na pytanie w jaki sposób powinniśmy chronić naszą prywatność, najpierw należy ustalić, czym właściwie są dane osobowe. To nic innego jak informacje, które odnoszą się do konkretnej osoby fizycznej, które pozwalają ją zidentyfikować np. numer PESEL, adres e-mail, wizerunek twarzy czy odcisk palca. Jednak już np. numer buta czy sama wysokość zarobków to zbyt ogólne informacje, zwłaszcza jeśli nie zestawimy ich z innymi, bardziej szczegółowymi, by ustalić, ze chodzi o nas. I wówczas nie zaliczamy ich już do danych osobowych.
</p>
    <img src="/img/articles/prywatnosc_w_sieci.jpg" class="article-img" alt="Prywatność w sieci"/>
<p>Mówiąc o danych nie sposób nie wspomnieć również o danych wrażliwych, stanowiących istotny zbiór wiedzy na nasz temat. Składają się na nie takie informacje jak np. nasz stan zdrowia czy historia przebytych chorób, ale też adnotacja o naszej orientacji seksualnej, wyznaniu czy kolorze skóry - są to bowiem dane bardzo prywatne, które w niewłaściwych rękach mogłyby zostać wykorzystane przeciwko nam.</p>

    <h2 class="subpage-subtitle" id="warto-chronic-dane">Warto chronić dane</h2>
    <p>
    No właśnie, tutaj należałoby wyjaśnić, dlaczego właściwie dane na nasz temat mogą być tak cenne. W końcu, o ile nie piastujemy eksponowanego stanowiska publicznego lub tez nie jesteśmy osobą powszechnie znaną, informacje o nas nie powinny nikogo interesować. W rzeczywistości jednak stanowią one cenny łup dla  przestępców, którzy mogą zaciągnąć pożyczkę na fałszywe dane lub dla portali informacyjnych, które na podstawie zdobytej wiedzy na nasz temat mogą podsuwać nam wybrane komunikaty i w efekcie zamknąć nas w bańce informacyjnej. 
</p>
    <h2 class="subpage-subtitle" id="jak-zatem-chronic-dane-osobowe-i-prywatnosc-w-sieci">Jak zatem chronić dane osobowe i prywatność w sieci?</h2>
<p>
Na nasz poziom bezpieczeństwa może mieć wpływ wiele naszych zachowań w internecie. Wystarczy bowiem, że zbyt wiele danych zdradzimy w poście opublikowanym w mediach społecznościowych. Wrzucane na bieżąco do sieci zdjęcia z egzotycznych wakacji mogą stanowić jasny sygnał dla przestępców, że to właśnie teraz jest najlepszy moment na włamanie się do naszego domu. Warto również pamiętać o tym, że publikowane zdjęcia w mediach społecznościowych mogą wpływać również na prywatność osób trzecich – zwłaszcza jeśli załapały się np. na fotografię, a ich twarz jest widoczna. Z tego powodu powinniśmy z dużą ostrożnością podchodzić do publikowania treści w internecie i minimalizować liczbę zdradzanych tam informacji. Dobrze też sprawdzić ustawienia prywatności naszego konta – i ograniczyć widoczność relacji np. do grona najbliższej rodziny i przyjaciół. A zdjęciami z wakacji pochwalić się dopiero, gdy będziemy już z powrotem w domu.
</p>
<p>
Nasze działania w sieci to także aktywność na smartfonie. Z tego powodu powinniśmy przyjrzeć się, do jakich opcji dostęp mają aplikacje na urządzeniu. Warto zastanowić się, czy komunikator tekstowy faktycznie potrzebuje zezwolenia na korzystanie z kamery lub mikrofonu, aplikacja sklepu zaś – danych o naszej lokalizacji. To, jaki zakres dostępu posiadają poszczególne aplikacje, łatwo sprawdzimy w ich ustawieniach – tam też w każdej chwili możemy ograniczyć ich uprawnienia. 
</p>
<p>
Myśląc o prywatności i ochronie danych w sieci, musimy zatem pamiętać o jednej podstawowej zasadzie – w dużej mierze to my sami decydujemy, którymi informacjami i z kim się dzielimy. Należy tylko mieć na uwadze, by sprawować nad tym kontrolę, a decyzji nie odejmować bez zastanowienia.
</p>

