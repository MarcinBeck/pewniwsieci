    <h2 class="subpage-subtitle" id="w-tych-sytuacjach-warto-zachowac-czujnosc">W tych sytuacjach warto zachować czujność</h2>
    <p>Materiały phishingowe są oczywiście przygotowywane w taki sposób, żeby były jak najbardziej wiarygodne. Mimo wszystko, są pewne elementy po których możemy je rozpoznać:</p>
    <ol>
    <li>Niezgodne źródło - zawsze sprawdzaj nadawcę i to, czy jego adres/nazwa nie zawiera np. literówek, błędów czy całkowicie niepoprawnej domeny (w przypadku maili).</li>
    <li>Błędy - wiadomości phishingowe często zawierają literówki, błędy interpunkcyjne oraz gramatyczne, a także językowe. Czasem wynikają one z nieznajomości języka, automatycznego tłumaczenia lub z pośpiechu – a czasem są umieszczane celowo, aby wyeliminować najbardziej uważnych i spostrzegawczych odbiorców z puli potencjalnych ofiar.</li>
    <li>Powołanie się na nieistniejące procedury - manipulacja może polegać na tym, że proszeni jesteśmy o podanie informacji, które rzekomo niezbędne są do przeprowadzenia procedur w ramach danej instytucji. Często jednak nie ma to nic wspólnego z rzeczywistymi procedurami. W sytuacji, w której jesteśmy pytani o poufne dane, proszeni  o instalację oprogramowania czy też kliknięcie w podejrzany link, istnieje zagrożenie, że jesteśmy celem ataku phishingowego.</li>
    <li>Wywoływanie poczucia nagłości - jest to często powiązane z powoływaniem się na nieistniejące procedury – bezsensowne prośby i próby manipulacji tracą na skuteczności, gdy mamy czas się nad nimi zastanowić. Jeśli w treści e-maila, sms-ie lub połączeniu znajdzie się informacja, np. że „TWOJE KONTO BANKOWE ZOSTAŁO ZABLOKOWANE PRZEZ PODEJRZENIE OSZUSTWA, KLIKNIJ [TUTAJ] I ZALOGUJ SIĘ, ABY SPRAWDZIĆ, W INNYM PRZYPADKU PRZEKAŻEMY SPRAWĘ POLICJI”, to powinna nam się zapalić czerwona lampka. </li>
    <li>Fałszywe linki  - link w samej wiadomości może wydawać się poprawny, ale warto zawsze chwilę zawiesić nad nim kursor i zobaczyć, czy podgląd linku się z nim zgadza – co do pojedynczego znaku! Linki mogą także różnić się pojedynczą literą, cyfrą lub symbolem.</li>
  </ol>
<p>
Warto oczywiście pamiętać, że doświadczony oszust będzie starał się przygotować dokument tak, żeby był jak najbardziej wiarygodny. Błędy mogą być oczywiste, ale dobrze przygotowana wiadomość może dla niewprawionego oka nie różnić się niczym od prawdziwej. Jeśli masz jakiekolwiek wątpliwości, skonsultuj się z reprezentantem instytucji, od której rzekomo pochodzi dokument, i pamiętaj:
</p>
<ul>
<li>Nie daj wciągnąć się w działanie pod presją czasu! Jak już wspomnieliśmy, pośpiech sprzyja błędom.</li>
<li>Nigdy nie podawaj wrażliwych danych takich jak np. dane logowania. Pracownik instytucji nigdy nie poprosi Cię o nie – nawet w sytuacji zagrożenia.</li>
<li>Zawsze sprawdzaj e-maile i strony. Szukaj symbolu kłódki oznaczającego bezpieczne, autoryzowane, szyfrowane połączenie.</li>

</ul>

