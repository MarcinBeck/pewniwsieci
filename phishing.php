  <p>
  Pozornie prawidłowa wiadomość czy link mogą się okazać przynętą służącą do kradzieży naszych danych. Jak więc nie dać się złowić? W tym artykule skupimy się na jednym z najpopularniejszych ataków wykorzystującym techniki wpływu społecznego – phishingu. Czym dokładnie jest, jak działa, w jakich celach jest stosowany i jak możemy zauważyć, że mamy z nim do czynienia?
</p>
<p>
Phishing jest formą oszustwa, opartą na działaniu inżynierii społecznej, w której atakujący kieruje do nas komunikat, podszywając się pod znaną nam instytucję np. bank, uczelnię, organ ścigania lub miejsce pracy. Celem tej metody, jak sugeruje nazwa, jest „złowienie” nas fałszywym pretekstem tak, abyśmy podali wrażliwe informacje, udzielili dostępu lub zainstalowali złośliwe oprogramowanie. 
</p>
<img src="/img/articles/phishing.jpg" class="article-img" alt="Phising"/>

<h2 class="subpage-subtitle" id="phishing-niejedno-ma-imie">Phishing niejedno ma imię</h2>


<p>Istnieją różne odmiany i rodzaje phishing’u takie jak vishing (phishing oparty na połączeniu głosowym, w którym oszust nakłania ofiarę do np. zainstalowania złośliwego oprogramowania wykorzystywanego na dalszym etapie oszustwa) oraz smishing (phishing wykorzystujący wiadomości SMS, najczęściej ze złośliwym linkiem). </p>
<p>Poniżej znajduje się przykład phishing’owego e-maila „podszywającego się” pod bank. Warto zwrócić uwagę na bardzo krótką informację z próbą nakłonienie użytkownika do podjęcia działań przez wywoływanie poczucia nagłości i powagi sytuacji. </p>
<p>Kolejna rzecz, na którą warto zwracać uwagę w wiadomościach, które podejrzewamy o bycie phishingowymi, są schowane linki. Link można ukryć np. poprzez wklejenie linku do tekstu i zrobienie z niego hiperlinka z załączonym innym linkiem. </p>
<p>Wiadomości phishingowe często zawierają informację o tym, że temat jest pilny i wymaga naszej szybkiej reakcji. Możemy otrzymać na przykład taki komunikat: „Zweryfikuj swoje konto w ciągu 20 minut albo zostanie ono zablokowane.” W ten sposób przestępca chce zadbać o to, że nie będziemy mieli czasu na sprawdzenie poprawności tej informacji.</p>


