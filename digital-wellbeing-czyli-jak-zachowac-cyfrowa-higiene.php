  <p>
  Po czasach, w których uczyliśmy się korzystać z dobrodziejstw świata technologii, nastał okres, podczas którego wielu z nas nie wyobraża sobie życia bez internetu. Telefon, który kiedyś służył wyłącznie do wykonywania połączeń, aktualnie pomaga w niemal każdej czynności – od załatwienia sprawy w banku czy urzędzie, przez dostarczanie rozrywki, aż po przypominanie o dziennej liczbie kroków do wykonania. I chociaż nie ma wątpliwości, że internet to ogromne ułatwienie w codziennym życiu, w zbyt dużych dawkach potrafi zachwiać równowagę między światem online, a rzeczywistością. Jak do tego nie dopuścić?
</p>

<img src="img/articles/higiena_cyfrowa.jpg" class="article-img" alt=""/>

<p>6 godzin i 42 minuty<sup>1</sup> – średnio tyle czasu dziennie spędza online przeciętny polski użytkownik internetu. Z raportu Digital 2023: Poland dowiemy się też, że 2 godziny i 2 minuty z tego czasu poświęcamy na social media. Ten sam raport dodaje, że z mediów społecznościowych korzysta 27,5 mln Polaków, czyli aż 66,3% całej populacji<sup>2</sup>. Wniosek nasuwa się sam – kochamy być w internecie i korzystać z jego dobrodziejstw. </p>
<p>Jak ze wszystkim co lubimy, również i z obecnością w cyfrowej sieci można przesadzić. W konsekwencji może to doprowadzić do przeciążenia naszego umysłu, a nawet różnych zaburzeń. FOMO, doomscrolling czy nomophobia to tylko niektóre ze skutków, jakie mogą być wywołane przez nadmierne korzystanie z mediów społecznościowych. </p>


<h2 class="subpage-subtitle" id="co-to-oznacza">Co to oznacza?</h2>
<p>FOMO (z ang. fear of missing out) to strach przed tym, co nas omija. Wiąże się z potrzebą ciągłego sprawdzania aktualnych wydarzeń ze świata, ale również tego, co słychać u naszych bliższych lub dalszych znajomych. Najbardziej podatne na FOMO są nastolatki, które spędzają w sieci ponad 5 godzin dziennie<sup>3</sup>. Według danych zawartych w raporcie „FOMO 2022. Polacy a lęk przed odłączeniem”<sup>4</sup>, jedynie 6% nastolatków nie doświadcza tego zjawiska, a co trzeci doświadcza go bardzo silnie. Uwagę rodziców powinien wzbudzić fakt ciągłego przebywania dziecka z telefonem, nawet w sytuacjach takich jak rodzinny obiad.</p>
<p>Z kolei doomscrolling to poświęcenie nadmiernej ilości uwagi i czasu na przyswajanie negatywnych, niepokojących informacji ze świata mediów. W niepewnych czasach, w których żyjemy, bardzo łatwo jest skupić się jedynie na poszukiwaniu wiadomości, które mają wpływ na nasze poczucie bezpieczeństwa. Może to jednak skutkować znacznym obniżeniem nastroju, a nawet pojawieniem się stanów lękowych.</p>
<p>Pod pojęciem nomophobia kryje się strach przed brakiem dostępu do telefonu komórkowego. Wiąże się z tym dyskomfort psychiczny lub uczucie paniki, na przykład w momencie zgubienia smartfona lub braku możliwości skorzystania z niego.</p>




<h2 class="subpage-subtitle" id="wiemy-coraz-więcej">Wiemy coraz więcej</h2>

<p>Świadomość na temat zagrożeń związanych z nadużywaniem internetu jest coraz większa, naturalnie więc powstaje więcej pomysłów na to, jak sobie z tym radzić. Najpopularniejszy z nich to detoks cyfrowy, czyli postanowienie o niekorzystaniu ze smartfona przez określoną ilość czasu. Fundacja Dajemy Dzieciom Siłę zorganizowała nawet Offline Challange<sup>5</sup>, mający zachęcić nastolatków do odłożenia swojego telefonu na 48 godzin. Większość nowych urządzeń jest również wyposażona w narzędzia mające ułatwić korzystanie z nich w duchu digital wellbeing. Smartfony zliczają czas poświęcony na korzystanie z danej aplikacji, wysyłają powiadomienia zwracające uwagę na zbyt dużą liczbę godzin przed ekranem lub dają możliwość zablokowania konkretnej aplikacji w wybranych godzinach.</p>
<p>Trend ten jest obecny również w niektórych miejscach publicznych – część restauracji zaczęła wymagać od swoich klientów, aby na czas posiłku oddawali telefony kelnerowi i spędzili ten czas na rozmowie z najbliższymi.</p>


<h2 class="subpage-subtitle" id="jak-tego-uniknac">Jak tego uniknąć?</h2>

<p>Ponieważ internet na dobre zawitał w naszm życiu, ułatwia wiele jego aspektów i nikt nie chce rezygnować z niego w całości. Warto jednak zastosować kilka zasad, które sprawią, że korzystanie z sieci nie będzie dla nas szkodliwe:</p>
<ol>
<li>Kontroluj czas, jaki spędzasz przed telefonem – możesz w tym celu korzystać np. z aplikacji informujących o przekroczeniu wyznaczonej liczby minut/godzin.</li>
<li>Ogranicz liczbę powiadomień w ustawieniach telefonu i usuń aplikacje, z których nie korzystasz.</li>
<li>W nocy trzymaj telefon z dala od łóżka i nie korzystaj z niego na 1-2 godziny przed snem.</li>
<li>Spotykając się z rodziną lub znajomymi, nie wyjmuj telefonu, jeśli nie jest to konieczne. Dbanie o relacje z bliskimi jest bardzo ważne w przeciwdziałaniu uzależnieniu od internetu.</li>
<li>Posiłki spożywaj bez telefonu w zasięgu wzroku.</li>
<li>Sprawdzaj, czy wśród treści, które do Ciebie docierają, nie przeważają wyłącznie negatywne komunikaty.</li>
<li>Planuj czas na regularną aktywność fizyczną.</li>
<li>Co jakiś czas organizuj sobie cyfrowy detoks.</li>
<li>Spędzaj czas wolny bez telefonu: czytaj książki, rozwiązuj krzyżówki, spaceruj, spotkaj się z bliskimi.</li>
<li>Nie korzystaj z telefonu podczas jazdy samochodem lub rowerem.</li>
</ol>
