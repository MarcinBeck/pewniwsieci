  <p>
  Sztuczna inteligencja od jakiegoś czasu pobudza wyobraźnię nie tylko fanów science-fiction, ale też naukowców czy przedsiębiorców. Dzięki przełomowi, którym było pojawienie się w szerokim użyciu takich narzędzi jak Chat GPT pozwalającego na rozmowę z AI czy usługi Midjourney, która potrafi wygenerować dowolny wymyślony przez nas obraz, temat sztucznej inteligencji szybko trafił pod strzechy. Jakie szanse wynikają z popularyzacji tego rozwiązania? Jakie są zaś zagrożenia i jak się przed nimi bronić?
</p>

<!--<img src="img/articles/no-img.jpg" class="article-img" alt=""/>-->

<p>Sztuczna inteligencja nie ma jednej, oficjalnej definicji. W uproszczeniu pozwala ona maszynom na uczenie się na podstawie „doświadczeń”, dostosowywanie się do nowych informacji i wykonywanie zadań podobnych do ludzkich. Jak wynika z danych IBM, w 2022 roku w ujęciu globalnym 35% firm aktywnie korzystało z rozwiązań z zakresu sztucznej inteligencji, a 42% przedsiębiorców badało zastosowanie AI<sup>1</sup>. Nie ma wątpliwości, że odpowiednie wykorzystanie sztucznej inteligencji może pomóc w optymalizacji wielu procesów w firmach i w codziennym życiu, ale też w osiągnięciu większych, globalnych celów. </p>
<p>Spekuluje się, że dzięki sztucznej inteligencji możliwe będzie na przykład przeciwdziałanie brakowi wody na świecie poprzez monitorowanie opadów i opracowywanie optymalnej metody ich wykorzystywania. Może też być użyta do tego, aby redukować problem smogu czy efektywnie zarządzać zużyciem energii.</p>
<p>Niektórzy obawiają się, że w związku z rozwojem tej technologii wiele zawodów zostanie wypartych, jednak dane Światowego Forum Ekonomicznego rozwiewają te wątpliwości. Według ich danych do 2025 roku dzięki automatyzacji powstanie aż 12 milionów więcej miejsc pracy, niż ich zniknie<sup>2</sup>. Wielu specjalistów twierdzi, że zawód prompt managera, czyli specjalisty ds. kontaktu z AI, będzie jednym z kluczowych w przyszłości.</p>
<p>Rozwój sztucznej inteligencji ma związek nie tylko z przedsiębiorstwami czy ratowaniem świata – jego zadaniem jest również ułatwienie naszego codziennego życia. Chociaż do przyszłości rodem z filmów sci-fi, w których w każdym gospodarstwie domowym będzie dodatkowy, zrobotyzowany członek rodziny, jeszcze nam daleko, to już teraz możemy korzystać z opartych na AI asystentach głosowych. Według serwisu statista.com do 2024 roku na globalnym rynku będzie działać 8,4 miliarda cyfrowych asystentów<sup>3</sup> – tak więc ich liczba przekroczy liczbę ludności na świecie.</p>

</p>

<h2 class="subpage-subtitle" id="dwie-strony-medalu">Dwie strony medalu</h2>
<p>Warto jednak pamiętać, że nowe technologie to nie tylko szanse, ale też zagrożenia – jednak to związane z potencjalnym przejęciem władzy nad ludzkością przez maszyny jest na ten moment mało prawdopodobne. Niemniej już teraz zauważalne jest wykorzystanie AI przy tworzeniu tzw. deepfake'ów, czyli filmów przedstawiających prawdziwie wyglądające postacie/twarze, a nawet znane osoby, które mają przekazywać bezsensowne informacje w „swoich” wystąpieniach. Dalszy rozwój sztucznej inteligencji niesie ze sobą ryzyko coraz bardziej ekstremalnych prób oszustw i dezinformacji, co z pewnością budzi niepokój wielu osób. 
</p>
<p>Wśród obaw wymienianych przez ekspertów wskazuje się również na brak wiedzy na temat tego, w jaki sposób przetwarzane są dane wykorzystywane przez sztuczną inteligencję. Ma to znaczenie nie tylko w związku z ochroną danych osobowych, ale też w przypadku wszelkiego rodzaju tzw. scoringów, czyli algorytmów obliczających wartość użytkownika, np. w kontekście decyzji kredytowych czy ubezpieczeń zdrowotnych.</p>

<p>O tym, że temat jest bardzo ważny, wskazuje fakt, że Parlament Europejski pracuje już nad odpowiednimi regulacjami w tym zakresie, tzw. „AI Act”, które mają ograniczać wykorzystanie tej technologii, tak aby chronić użytkowników. Ostateczne wejście w życie nowego prawa może jeszcze potrwać – nadal prowadzone są konsultacje w tej sprawie. Dlatego kluczowa jest oczywiście edukacja – powinniśmy jak najwięcej dowiadywać się o sztucznej inteligencji i metodach dezinformacji i uczulać na ten problem naszych najbliższych.
</p>
<h2 class="subpage-subtitle" id="co-powinnismy-wiedziec">Co powinniśmy wiedzieć?</h2>

<p>Czekanie na zmiany legislacyjne nie jest jedynym rozwiązaniem, które możemy podjąć. Już teraz warto przestrzegać kilku reguł, które pozwolą na minimalizowanie ryzyka związanego z rozwojem sztucznej inteligencji.</p>

<ol>
<li>Nie wprowadzaj do systemów takich jak Chat GPT czy Midjourney lub innych opartych na sztucznej inteligencji danych chronionych przez prawo (danych osobowych, danych poufnych itp.) – o ile nie masz pewności, że to bezpieczne.</li>
<li>Korzystaj z wyników działania AI tylko wtedy, jeśli wiesz, że są prawdziwe.</li>
<li>Informacje dostarczone przez systemy oparte na sztucznej inteligencji weryfikuj w innym źródle.</li>
<li>Nie powielaj treści pozyskanych przez sztuczną inteligencję w internecie, o ile nie masz pewności, że nie wprowadzą nikogo w błąd lub nie naruszają czyichś praw autorskich.</li>
<li>Przesyłając dalej treści wygenerowane przez AI oznacz je stosownym komentarzem.</li>
</ol>

<p>Zapraszamy do obejrzenia odcinka z tej serii „Pewni w sieci”, w którym Aleksandra Przegalińska, futurolożka, opowiada dokładniej o sztucznej inteligencji i tego, do czego może być wykorzystywana.</p>




