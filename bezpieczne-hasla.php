    <p>
    Jednym z najstarszych, a jednocześnie wciąż najważniejszym mechanizmem zapewniającym bezpieczeństwo naszych danych są hasła. Powszechnie wiadomo, że hasło powinno być bezpieczne i okresowo zmieniane. Wiemy też, że nie powinniśmy ujawniać go osobom trzecim. Ale czy to wszystko? W mediach wielokrotnie pojawiały się doniesienia o wyciekach danych, spowodowanych właśnie niefrasobliwym podejściem do haseł. Czego więc powinniśmy unikać, a na co zwrócić uwagę, by nasze hasła były bezpieczne?

    </p>
    <p>
    Wielu z nas pamięta na pewno przejęcie przez niezidentyfikowanych dowcipnisiów strony internetowej Kancelarii Premiera. Jeśli wierzyć opublikowanym przez nich wiadomościom, login dostępu do strony brzmiał: „admin” a hasło – „admin1”. Choć może wydawać się to zabawne, jednak niestety bywa to praktyka powtarzana nie tylko w urzędach, czy instytucjach państwowych. W czasach, gdy każdy z nas musi zapamiętać kilkadziesiąt haseł, do różnorakich serwisów, urządzeń czy usług, nie dziwi, że wiele osób idzie na łatwiznę, stosując na przykład jedno, dwa hasła do wszystkiego. Pamiętajmy, że działając w ten sposób otwieramy drogę do naszych danych nie tylko „trollom”, ale przede wszystkim przestępcom. 
</p>
   
    <img src="/img/articles/bezpieczne_hasla.jpg" class="article-img" alt="Bezpieczne hasła"/>

    <h2 class="subpage-subtitle" id="zasady-mocnego-hasla">Zasady mocnego hasła</h2>
<p>
Pamiętajmy, że hasło dla każdej usługi czy serwisu powinno być unikatowe – starajmy się unikać prostego dodawania np. wykrzyknika do już stosowanych haseł. Unikajmy haseł łatwych do odgadnięcia, prostych słów, takich jak „hasło” czy ciągów znaków (abcdefgh czy qwertyu). Nie wykorzystujmy też naszych danych osobowych, imion czy dat (np. urodzin). Hakerzy mają specjalnie przygotowane „słowniki” najbardziej popularnych haseł, a także sprawdzają ich modyfikacje, np.: tomasz, Tomasz1, Tomasz123, Tomasz$$$.
</p>
<p>Najtrudniejszym dla cyberprzestępców hasłem będzie krótkie zdanie, ale nie zaczerpnięte z literatury tylko unikalne dla nas samych. Tak samo jak technologia rozwija się bardzo szybko, tak samo hakerzy aktualizują swoją wiedzę na bieżąco. Do niedawna rekomendowane były hasła typu: litwoojczyznomoja, ktotyjestespolakmaly, showmustgoon, anotheronebitesthedust, jednak hakerzy już się ich nauczyli i mają je w swoich słownikach. Najlepszym obecnie wyborem będzie wybranie hasła-zdania unikatowego dla nas, np.: lubiegotowanabrukselke, codzienniewstajeo6rano, mojkotnazywasiekot. Są to hasła łatwe do zapamiętania dla nas, zaś trudne dla cyberwłamywaczy, ponieważ mają zbyt dużo wariantów. </p>




    <h2 class="subpage-subtitle" id="nie-trzymaj-swoich-hasel-na-pulpicie">Nie trzymaj swoich haseł na pulpicie!</h2>
    <p>
    Jednym z najczęstszych błędów jest nieprawidłowe przechowywanie danych uwierzytelniających, loginów i haseł. Pamiętaj, żeby haseł nie zapisywać w pliku (np. excela czy notatnika). Najlepiej wykorzystać do tego rozwiązanie, często udostępniane wraz z przeglądarką internetową, które umożliwia zapisywanie naszych haseł. Można wykorzystać też specjalne narzędzia, takie jak na przykład Norton Security Online, który posiada funkcję manager’a haseł. Większość zabezpieczeń tego typu posiada też wersje na urządzenia mobilne, integruje się z naszą przeglądarką oraz zapewnia automatyczne logowanie. Użytkownik musi więc pamiętać tylko jedno, główne hasło do takiego sejfu danych – resztę zrobi za niego sama aplikacja. 

</p>
    <h2 class="subpage-subtitle" id="zmien-haslo-domyslne">Zmień hasło domyślne</h2>
    <p>
    Często zdarza się, że producent sprzętu (np. routera WiFi) lub usługi przekazuje użytkownikowi domyślne hasło. Niestety, nie zawsze wymagana jest jego zmiana przy pierwszym logowaniu, a w wypadku urządzeń sieciowych użytkownicy często nie wiedzą nawet o tym, że mogą zmieniać ustawienia po zalogowaniu się. W sieci łatwo można znaleźć spisy takich domyślnych ustawień, posortowane według producentów, modeli urządzeń czy rodzajów aplikacji. Jeśli pominiemy zmianę domyślnego hasła podczas instalacji i konfiguracji urządzenia lub aplikacji, dajemy nawet niedoświadczonym włamywaczom zielone światło. Co gorsza, tak niezabezpieczony sprzęt często pada ofiarą bardziej zaawansowanych przestępców, staje się częścią botnetu, i sam infekuje kolejne, połączone z nim urządzenia. </p>
    <h2 class="subpage-subtitle" id="uwierzytelnianie-dwuskladnikowe">Uwierzytelnianie dwuskładnikowe</h2>
<p>Jedną z najlepszych metod na zwiększenie bezpieczeństwa naszych danych jest wykorzystywanie wieloskładnikowego uwierzytelnienia. Dzięki temu, logowanie do najważniejszych usług (np. bankowości elektronicznej, głównej skrzynki pocztowej, portalu społecznościowego) wymagać będzie dodatkowego potwierdzenia za pomocą specjalnie wygenerowanego, losowego kodu, wysyłanego na nasz adres e-mail lub urządzenie mobilne. </p>



    <h2 class="subpage-subtitle" id="przypomnijmy-najważniejsze-zalecenia-dotyczace-hasel">Przypomnijmy najważniejsze zalecenia dotyczące haseł:</h2>
<ol>
<li>Unikaj zbyt prostych haseł, nawet, jeśli ustawiasz je dla pozornie nieistotnej usługi.</li>
<li>Nie wysyłaj nikomu swoich haseł.</li>
<li>Pamiętaj, że administrator usługi czy serwisu nigdy nie poprosi Cię o podanie hasła np. w e-mailu.</li>
<li>Wykorzystuj unikatowe, lub generowane losowo, hasła dla każdej usługi czy serwisu. Ich przechowywanie powierz „menadżerowi haseł”.</li>
<li>Zawsze zmieniaj hasła domyślnie ustawione przez administratorów lub przez producenta sprzętu.</li>
</ol>
