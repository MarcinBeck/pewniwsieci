    <h1 class="subpage-title">Bezpieczne relacja i komunikacja online. Jak zachować ostrożność w poznawaniu kogoś przez internet?</h1>
    <nav id="subpage-nav">
      <ul >
        <li><a href="#grooming-co-to-jest">Grooming - co to jest?</a></li>
        <li><a href="#badzmy-uwazni-w-sieci">Bądźmy uważni w sieci</a></li>
      </ul>
    </nav>
  </div>
  <p>
  „Życie” w świecie wirtualnym toczy się właściwie równolegle do naszego życia offline. Tak, jak w realu nawiązujemy relacje w pracy, w szkole, podczas różnych zajęć, tak też „poznawanie” kogoś w sieci jest już czymś normalnym. Ale czy na pewno bezpiecznym? Niestety nie zawsze, dlatego warto wiedzieć o podstawowych zasadach komunikacji z osobami poznanymi online i ewentualnych zagrożeniach z tego wynikających. 
</p>

<img src="img/articles/bezpieczne_relacje.jpg" class="article-img" alt=""/>


<p>
Nawiązywanie relacji w internecie jest dość powszechnym zjawiskiem – istnieją różne grupy zainteresowań, czy aplikacje do poznawania innych osób. Na pierwszy rzut oka powinna być to bezpieczna droga, bo przecież „tylko” z kimś piszemy na czacie. Jednakże nigdy nie mamy pełnej pewności, kto znajduje się z drugiej strony komunikatora i jakie ma intencje. Najbardziej na zagrożenia związane z tworzeniem relacji online są dzieci i nastolatkowie, którzy często zmagają się np. z niskim poczuciem własnej wartości, a nowo poznany „przyjaciel” daje im swój czas oraz zainteresowanie. Niestety może to być cyberprzestępca, który manipuluje dzieckiem, aby np. zdobyć o nim jakieś informacje bądź nagie zdjęcia. A zjawisko to staje się coraz bardziej powszechne – jak wynika z raportu przygotowanego przez NASK „Nastolatki 3.0” (2023)<sup>1</sup>, aż 17,9% nastolatków decyduje się na spotkanie z nieznajomym dorosłym z internetu, a co czwarty z nich nikogo o tym spotkaniu nie poinformował. 


</p>

<h2 class="subpage-subtitle" id="grooming-co-to-jest">Grooming - co to jest?</h2>
<p>Zjawisko polegające na podejmowaniu działań w internecie, które służą nawiązaniu więzi emocjonalnej z dzieckiem, aby zmniejszyć jego opory i później je seksualnie wykorzystać, to grooming. Jest to także mechanizm używany po to, by nakłonić dziecko do prostytucji czy udziału w pornografii dziecięcej. Groomer nawiązuje kontakt z dzieckiem i buduje z nim relacje. Tylko że ta relacja ma ukryty cel i nie jest to zwykle czysta sympatia. Groomer poznając bliżej dziecko, poznaje przede wszystkim jego potrzeby, by w niedługim czasie wykorzystać tę wiedzę przeciwko niemu. Manipuluje on swoją ofiarą, wykorzystuje zastraszanie bądź szantaż, aby osiągnąć swój cel. Co niezwykle ważne, grooming dotyczy nie tylko dziewczynek – narażone jest na to każde dziecko, bez względu na wiek czy płeć. Co można więc zrobić, aby ochronić dziecko przed tym zjawiskiem? </p>


<h2 class="subpage-subtitle" id="badzmy-uwazni-w-sieci">Bądźmy uważni w sieci</h2>
<p>
Choć internet jest obecny w różnych sferach życia dzieci i młodzieży, to warto rozmawiać z nimi o tym, jak świadomie i bezpiecznie nawiązywać relacje online. I budować z nimi więź opartą na zaufaniu, bowiem jak wynika z raportu NASK<sup>2</sup>, kiedy nastolatki doświadczają przemocy najczęściej nie reagują i nikomu o tym nie mówią. Co więcej, bierność w sytuacjach doświadczania przemocy w sieci wzrasta w stosunku do poprzednich lat (2022 – 38,5% vs. 2020 – 32,4%).
Co więc możemy zrobić? Oto kilka prostych zasad: 


</p>
<ol>
<li>Jeśli zauważymy, że ktoś z naszego otoczenia – dziecko, przyjaciel, spędza dużo czasu w internecie konwersując z nieznaną osobą, bądźmy ostrożni, porozmawiajmy z bliskim, wyjaśnijmy  zagrożenia wynikające z takiego zachowania. </li>
<li>Jeśli jakaś osoba bądź prowadzona z nią konwersacja staje się dziwna, czujemy, że ktoś zachowuje się nieodpowiednio, niezwłocznie zablokujmy takie konto i usuńmy tę konwersację. </li>
<li>Rozmawiajmy z dziećmi o tym, dlaczego należy być czujnym i ostrożnym w nawiązywaniu relacji online – dla nich to zwykła zabawa, która realnie może doprowadzić do czegoś złego w skutkach. </li>
<li>Nie ufajmy we wszystko, co pisze osoba poznana w internecie – cyberprzestępcy znają się na technikach manipulacji i zdobywania zaufania swoich ofiar.</li>
<li>Nie klikajmy w żadne linki, które ktoś przesyła w takiej konwersacji – może to był próba przechwycenia naszych haseł, włamania się na nasze konta itp. </li>

</ol>


<p>
To tylko kilka prostych kroków, dzięki którym można ustrzec dzieci i młodzież przed pułapką nieodpowiednich relacji w sieci. Kluczowa jest przede wszystkim rozmowa i edukacja – zarówno w domu, jak i w szkole. </p>
